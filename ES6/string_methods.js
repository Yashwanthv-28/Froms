const str = "Hello, JavaScript is awesome!";

// Check if a string starts with a specific word
console.log(str.startsWith("Hello"));

// Check if a string ends with a specific word
console.log(str.endsWith("awesome!")); 

// Check if a substring exists within the string
console.log(str.includes("JavaScript")); 

// Repeat the string multiple times
console.log("Hello ".repeat(3)); 

// Using template literals for dynamic text
const language = "JavaScript";
console.log(`I love ${language}!`); 